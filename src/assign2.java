public class assign2 {
    public static void main(String[] args) {
        System.out.println("numbers before swapping");
        int a=10;
        int b=20;
        System.out.println("a:"+a);
        System.out.println("b:"+b);

        System.out.println("numbers after swapping");
        int temp=a;
        a=b;
        b=temp;
        System.out.println("a:"+a);
        System.out.println("b:"+b);

    }
}
