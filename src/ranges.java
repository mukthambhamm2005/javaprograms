public class ranges {
    public static void main(String[] args) {
        System.out.println("----------------------");
        byte a=127;
        System.out.println("example byte value "+ a);
        System.out.println("minimum byte value "+ Byte.MIN_VALUE);
        System.out.println("maximum byte value "+ Byte.MAX_VALUE);

        short len=3200;
        System.out.println("example short value "+ len);
        System.out.println("minimum short value "+ Short.MIN_VALUE);
        System.out.println("maximum short value "+ Short.MAX_VALUE);

        int age=34;
        System.out.println("example int value "+age);
        System.out.println("minimum int value "+Integer.MIN_VALUE);
        System.out.println("maximum integer value "+Integer.MAX_VALUE);

        float range=23;
        System.out.println("example float value "+range);
        System.out.println("minimum float value "+Float.MIN_VALUE);
        System.out.println("maximum float value "+Float.MAX_VALUE);

        long range2=23;
        System.out.println("example long value "+range2);
        System.out.println("minimum long value "+Long.MIN_VALUE);
        System.out.println("maximum long value "+Long.MAX_VALUE);

        double range3=23;
        System.out.println("example double value "+range3);
        System.out.println("minimum double value "+Double.MIN_VALUE);
        System.out.println("maximum double value "+Double.MAX_VALUE);

        boolean bool=true;
        System.out.println("example boolean value "+bool);

        char ch='a';
        System.out.println("example"+ ch);
        System.out.println("min value"+(int)Character.MIN_VALUE);
        System.out.println("max value"+(int)Character.MAX_VALUE);

        System.out.println((char)10888);
        System.out.println((char)10808);
        System.out.println((char)10085);
        System.out.println((char)10086);
        System.out.println((char)10087);
        System.out.println((char)10088);


    }
}
