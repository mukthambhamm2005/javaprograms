public class alldatatypes {
    public static void main(String[] args) {
        byte smallnumber=127;
        System.out.println("byte value "+ smallnumber);

        short shortnumber=32000;
        System.out.println("short value "+ shortnumber);

        int age=20;
        System.out.println("int value "+ age);

        long population=2000989;
        System.out.println("long value " + population);

        float price = .94453455f;
        System.out.println("float value " + price);

        double pi=3.141596754;
        System.out.println("double value " + pi);

        char grade='A';
        System.out.println("char value " + grade);

        boolean isEven=true;
        System.out.println("is even "+ isEven);

        String name="4Ai23cs000";
        System.out.println("String value "+ name);
    }
}
